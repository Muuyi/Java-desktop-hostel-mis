    //INSERTING ROOM NUMBERS INTO THE DATABASE
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String sql = "insert into rooms(rm_cat, rm_name, rm_amount) values(?,?,?)";
        try{
            pst = connection.prepareStatement(sql);
            pst.setString(1, (String) clRm.getSelectedItem());
            pst.setString(2, rm_no.getText());
            pst.setString(3, rm_amount.getText());
            JOptionPane.showMessageDialog(null, "A room has been successfully added!");
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed
