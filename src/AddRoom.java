    private void rm_amountKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_rm_amountKeyTyped
        char iNumber = evt.getKeyChar();
        if(!(Character.isDigit(iNumber))
        ||(iNumber == KeyEvent.VK_BACK_SPACE)
        || (iNumber == KeyEvent.VK_DELETE)){
            evt.consume();
        }
    }//GEN-LAST:event_rm_amountKeyTyped

